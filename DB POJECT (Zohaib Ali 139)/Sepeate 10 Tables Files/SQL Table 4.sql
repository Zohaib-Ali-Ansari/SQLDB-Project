CREATE DATABASE ECOM_PLATFORM;
USE ECOM_PLATFORM;

-- ORDER TABLE(HAVING ALL THE ORDER DETAILS)
CREATE TABLE Orders (
    OrderID INT AUTO_INCREMENT PRIMARY KEY,
    UserID INT,
    OrderDate DATE,
    TotalAmount DECIMAL(10, 2),
    FOREIGN KEY (UserID) REFERENCES Users(UserID)
);



